def get_modality_metadata(name: str):
    """Return ``(wavelengths, input_res, subpatches)`` for a known modality.

    Raises ``KeyError`` if the modality is not in the registry — pass the
    info manually to ``model.encode(..., wavelengths={...}, ...)`` in that
    case, or add the modality to this registry.
    """
    if name not in WAVELENGTHS:
        raise KeyError(
            f"Unknown modality {name!r}. Known: {sorted(WAVELENGTHS)}"
        )
    return (
        list(WAVELENGTHS[name]),
        float(INPUT_RES[name]),
        int(SUBPATCHES.get(name, 1)),
    )


def list_modalities():
    """Return the sorted list of modality names known to the registry."""
    return sorted(WAVELENGTHS)
